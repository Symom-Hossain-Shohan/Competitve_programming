bool func(vector<ll> v, ll m)
{
    vector<ll> nw;
    nw=v;
    for(int i=(int)v.size()-1;i>=2;i--)
    {
        if(nw[i]<m) return false;
        ll x=min(v[i],nw[i]-m)/3;
        nw[i-1]+=x;
        nw[i-2]+=2*x;
    }
    for(auto i: nw) if(i<m) return false;
    return true;
}

