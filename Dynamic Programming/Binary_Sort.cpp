#include<bits/stdc++.h>
using namespace std;

typedef long long ll;
const ll N = 1001; 
ll ar[N], n; 




void solve()
{
    cin >> n; 
    for(int i=0;i<n; i++) cin >> ar[i];


}

int main()
{
    ios_base::sync_with_stdio(false); cin.tie(NULL);
    ll tc = 1;
    cin >> tc;
    for (ll t = 1; t <= tc; t++)
    {
        cout << "Case " << t << ": ";
        solve();
    }
    return 0;
}